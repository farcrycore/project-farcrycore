CREATE TABLE dmCron(
ownedby nvarchar(250) NULL DEFAULT NULL ,
frequency nvarchar(250) NULL DEFAULT 'daily' ,
lastupdatedby nvarchar(250) NOT NULL DEFAULT '' ,
description nvarchar(MAX) NULL ,
parameters nvarchar(250) NULL DEFAULT NULL ,
datetimelastupdated datetime NOT NULL DEFAULT {ts '2214-05-05 18:06:41'} ,
template nvarchar(250) NULL DEFAULT NULL ,
label nvarchar(250) NULL DEFAULT NULL ,
createdby nvarchar(250) NOT NULL DEFAULT '' ,
startDate datetime NOT NULL DEFAULT {ts '2214-05-05 18:06:41'} ,
bAutoStart bit NOT NULL DEFAULT 1 ,
datetimecreated datetime NOT NULL DEFAULT {ts '2214-05-05 18:06:41'} ,
lockedBy nvarchar(250) NULL DEFAULT NULL ,
locked bit NOT NULL DEFAULT 0 ,
title nvarchar(250) NULL DEFAULT NULL ,
endDate datetime NOT NULL DEFAULT {ts '2214-05-05 18:06:41'} ,
timeOut decimal(11,0) NULL DEFAULT 60 ,
ObjectID nvarchar(50) NOT NULL DEFAULT '' 
);
